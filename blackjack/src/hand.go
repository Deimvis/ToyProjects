package bj

import (
	"strings"

	"github.com/Deimvis/toyprojects/deck_of_cards/deck"
)

type Hand []deck.Card

func (h Hand) String() string {
	strs := make([]string, len(h))
	for i := range h {
		strs[i] = h[i].String()
	}
	return strings.Join(strs, ", ")
}

func (h *Hand) Add(c deck.Card) {
	*h = append(*h, c)
}

